//multiplicatins of 2 float values

class Mfloat
{
	public static void main(String args[])
	{
		double f1=12.3;
		double f2=5.5;
		double p=f1*f2;
		System.out.println("The product of f1 and f2 =" +p);
		System.out.println("The ceiling value of p =" +Math.ceil(p));
		System.out.println("The floor value of p =" +Math.floor(p));

	}
}
